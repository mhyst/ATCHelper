class Model1 extends Model {
    public String title1, title2, title3, title4;

    public Model1(String title1, String title2, String title3, String title4) {
        super(null);
        this.title1 = title1;
        this.title2 = title2;
        this.title3 = title3;
        this.title4 = title4;
    }
    
    @Override
    public Object[] getRow() {
        Object[] row = new Object[4];
        row[0] = title1;
        row[1] = title2;
        row[2] = title3;
        row[3] = title4;
        
        return row;
    }

    @Override
    public void setRow(Object[] row) {
        title1 = (String) row[0];
        title2 = (String) row[1];
        title3 = (String) row[2];
        title4 = (String) row[3];
    }
}

